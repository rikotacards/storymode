import { PostWithImage } from "@/components/PostWithImage/PostWithImage";
import { getPostByUsername, PostFromDbProps } from "@/firebase/db";
import Head from "next/head";
import React from "react";
import styles from "../styles/AddPost.module.css";

export async function getStaticPaths() {
  console.log('hi')
  const paths = ["/max"];
  return {
    paths,
    fallback: false,
  };
}

export async function getStaticProps({
  params,
}: {
  params: { username: string };
}) {
  const posts = await getPostByUsername(params.username);
  return {
    props: {
      posts,
    },
  };
}

export const Profile = ({posts}) => {
  const postWithImage = posts.map((postData, i) => <PostWithImage key={postData.postTime+i} author={postData.author} content={postData.content} postTime={postData.postTime} />);
  return (
    <>
      <Head>
        <title>Profile</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <div>{postWithImage}</div>
      </main>
    </>
  );
};

export default Profile;
